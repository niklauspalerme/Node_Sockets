/////////////////////////////////////////////////////////////
// Importaciones


const { config } = require('dotenv');
const Server = require('./models/server');



/////////////////////////////////////////////////////////////
// Función Principal


let main = () => {


    ////////////////////////////////
    //Accedemos al .env

    config();

    ////////////////////////////////
    //Variables

    const port = process.env.PORT || 8080
    const server = new Server();

    ////////////////////////////////
    //Funciones

    server.listen(port)

}


/////////////////////////////////////////////////////////////
// Ejecutamos Función Principal


main();